package Tasks;


public class Array01_ConcatTwoStrings {

    public static void main(String[] args) {

        String str1 = "Ali";
        String str2 = "Veli";

        System.out.println(concatTwoStrings (str1, str2));

    }


    public static String concatTwoStrings (String str1, String str2){

        return str1 + ", " + str2;

    }


}
